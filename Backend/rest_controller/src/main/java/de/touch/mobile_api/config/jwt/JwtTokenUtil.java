package de.touch.mobile_api.config.jwt;

import java.io.Serializable;
import java.util.Date;
import java.util.HashMap;
import java.util.Map;
import java.util.function.Function;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.stereotype.Component;

import de.touch.mobile_api.config.Constants;
import de.touch.mobile_api.config.Constants.AccountStatus;
import de.touch.mobile_api.config.Constants.Role;
import io.jsonwebtoken.Claims;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;

@Component
public class JwtTokenUtil implements Serializable {

	private static final long serialVersionUID = -2550185165626007488L;

	@Value("${jwt.secret}")
	private String secret;

	// retrieve username from jwt token
	public String getUsernameFromToken(String token) {
		return getClaimFromToken(token, Claims::getSubject);
	}

	// retrieve role from jwt token
	public Role getRoleFromToken(String token) {
		Map<String, Object> claims = getAllClaimsFromToken(token);
		if(claims.get("Role").equals("a")) return Role.Admin;
		else return Role.User;
	}

	// retrieve accountStatus from jwt token
	public AccountStatus getAccountStatusFromToken(String token) {
		Map<String, Object> claims = getAllClaimsFromToken(token);
		if(claims.get("Status").equals("d")) return AccountStatus.Deactivated;
		if(claims.get("Status").equals("r")) return AccountStatus.Deleted;
		if(claims.get("Status").equals("a")) return AccountStatus.Activated;
		if(claims.get("Status").equals("v")) return AccountStatus.Verified;
		else return AccountStatus.Blocked;
	}

	// retrieve expiration date from jwt token
	public Date getExpirationDateFromToken(String token) {
		return getClaimFromToken(token, Claims::getExpiration);
	}

	public <T> T getClaimFromToken(String token, Function<Claims, T> claimsResolver) {
		final Claims claims = getAllClaimsFromToken(token);
		return claimsResolver.apply(claims);
	}

	// for retrieveing any information from token we will need the secret key
	private Claims getAllClaimsFromToken(String token) {
		return Jwts.parser().setSigningKey(secret).parseClaimsJws(token).getBody();
	}

	// check if the token has expired
	private Boolean isTokenExpired(String token) {
		final Date expiration = getExpirationDateFromToken(token);
		return expiration.before(new Date());
	}

	// generate token for user with role
	public String generateToken(UserDetails userDetails, Role role, AccountStatus status) {
		Map<String, Object> claims = new HashMap<>();
		claims.put("Role", role.getDatabaseValue());
		claims.put("Status", status.getDatabaseValue());
		return doGenerateToken(claims, userDetails.getUsername());
	}

	// while creating the token -
	// 1. Define claims of the token, like Issuer, Expiration, Subject, and the ID
	// 2. Sign the JWT using the HS512 algorithm and secret key.
	// 3. According to JWS Compact
	// Serialization(https://tools.ietf.org/html/draft-ietf-jose-json-web-signature-41#section-3.1)
	// compaction of the JWT to a URL-safe string
	private String doGenerateToken(Map<String, Object> claims, String subject) {

		return Jwts.builder().setClaims(claims).setSubject(subject).setIssuedAt(new Date(System.currentTimeMillis()))
				.setExpiration(new Date(System.currentTimeMillis() + Constants.JWT_TOKEN_VALIDITY * 1000))
				.signWith(SignatureAlgorithm.HS512, secret).compact();
	}

	// validate token
	public Boolean validateToken(String token, UserDetails userDetails) {
		final String username = getUsernameFromToken(token);
		return (username.equals(userDetails.getUsername()) && !isTokenExpired(token));
	}
}